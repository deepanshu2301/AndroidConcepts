package com.dipanshu.heterogenousview;

public class Imageclaass {

   private String title,subtitle,url;

    public Imageclaass(String title, String subtitle, String url) {
        this.title = title;
        this.subtitle = subtitle;
        this.url = url;
    }


    public String getTitle() {
        return title;
    }

    public String getSubtitle() {
        return subtitle;
    }

    public String getUrl() {
        return url;
    }
}
